<h1>Comment s'inscrire au BDS</h1><br><br>

<h2>Etape 1 :</h2><br>
Fais remplir un certificat médical par un médecin. <a href="/uploads/fichiers/certificat.pdf" >(lien vers le certicat)</a><br><br><br><br>

<h2>Etape 2 :</h2><br>
Prépare un chèque à l'ordre de «BDS UTBM» de 30€ pour une cotisation à l'année ou 18€ si tu veux cotiser au semestre.<br><br><br><br>

<h2>Etape 3 :</h2><br>
Apporte nous le tout durant nos permanences dans nos bureaux situés sur les 3 sites. Tu recevras alors ta carte justifiant ton adhésion parmi nous.<br><br><br><br>

<h2>Etape 4 :</h2><br>
Éclate-toi et n'oublie pas : « Un esprit sain dans un corps sain »<br><br><br><br>


